package Class_Program;

import java.util.Scanner;

public class Sum1 {

	public static void main(String_Example[] args) {
		Scanner s = new Scanner(System.in);
		int n = s.nextInt();
		int result=1;
		for(int i=n;i>=n;i--){
			result *= i;	
		}
		System.out.println("sum="+result);
		s.close();
	}
}
